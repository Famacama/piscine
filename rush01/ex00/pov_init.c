void	pov_init(int pov[16], char **argv)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (argv[1][i])
	{
		if (argv[1][i] >= '0' && argv[1][i] <= '9')
			pov[j++] = argv[1][i] - 48;
		i++;
	}
}
