void	ft_putchar(char c);

void	print_error_msg(void)
{
	char	*msg;
	int		i;

	msg = "Error\n";
	i = 0;
	while (msg[i] != '\0')
	{
		ft_putchar(msg[i]);
		i++;
	}
}
